    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        //PokemonId variable
        int pokemonID;
        
        pokemonID = Integer.parseInt(jTextField1.getText());
        
        jLabel2.setText("");
        jLabel3.setText("");
        jLabel4.setText("");
        jLabel5.setText("");
        jLabel6.setText("");
        jLabel7.setText("");
        jLabel8.setText("");
        
        if (pokemonID >= 1 && pokemonID <= 151){
            Pokemon requestedPokemon = getPokemonInfo(pokemonID);
            
            jLabel3.setText("No. " + pokemonID + " " + requestedPokemon.getName());
            
            float height = requestedPokemon.getHeight()/10;
            jLabel4.setText("Height: " + height + " m");
            
            float weight = requestedPokemon.getWeight()/10;
            jLabel2.setText("weight: " + weight + " kg");
            
            jLabel5.setText("Gender ratio: " + requestedPokemon.getGender());
            
            if(requestedPokemon.getAbilityII() != null){
                jLabel6.setText("Abilities: " + requestedPokemon.getAbilityI() + " & " + requestedPokemon.getAbilityII());
            }
            else {
                jLabel6.setText("Ability: " + requestedPokemon.getAbilityI());
            }
            
            if(requestedPokemon.getEvolvesFrom() != null){
                jLabel7.setText("Evolves from: " + getPokemonInfo(requestedPokemon.getEvolvesFrom().getId()).getName());
            }
            
            if(requestedPokemon.getEvolvesTo() != null){
                for (Pokemon p : requestedPokemon.getEvolvesTo()) {
                    jLabel8.setText("Evolves at/by: " + requestedPokemon.getEvolvesBy() +
                            " into "+ getPokemonInfo(p.getId()).getName());
                }
            }
        }
        else {
            jLabel3.setText("Id needs to be between 0 and 152");
        }
        
    }//GEN-LAST:event_okButtonActionPerformed

